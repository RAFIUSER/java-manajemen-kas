    private void TabelAkunMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelAkunMouseClicked
        // TODO add your handling code here:
        int selectedRow = TabelAkun.getSelectedRow();
        if (selectedRow == -1) {
            return;
        }

        try {
            Object idValue = TabelAkun.getModel().getValueAt(selectedRow, 1);
            selectedId = Integer.parseInt(idValue.toString());

            String namaLengkap = (String) TabelAkun.getModel().getValueAt(selectedRow, 2);
            inputNamaLengkap.setText(namaLengkap);

            String username = (String) TabelAkun.getModel().getValueAt(selectedRow, 3);
            inputUsername.setText(username);

            String jabatan = (String) TabelAkun.getModel().getValueAt(selectedRow, 4);
            inputJabatan.setSelectedItem(jabatan);

            btnSimpan.setText("Update");

        } catch (NumberFormatException e) {
            System.err.println("Error saat mengambil data dari tabel: " + e.getMessage());
        }
    }//GEN-LAST:event_TabelAkunMouseClicked

