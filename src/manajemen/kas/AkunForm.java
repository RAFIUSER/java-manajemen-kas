    private void loadTableData() {
        String[] columnNames = {"No.", "ID", "Nama Lengkap", "Usernamme", "Jabatan"};
        DefaultTableModel model = new DefaultTableModel(null, columnNames);
        List<User> userList = userDAO.getAll();

        int no = 1;

        for (User u : userList) {
            Object[] row = new Object[]{
                no++,
                u.getId(),
                u.getNamaLengkap(),
                u.getUsername(),
                u.getJabatan()
            };

            model.addRow(row);
        }

        TabelAkun.setModel(model);

        TabelAkun.getColumnModel().getColumn(0).setMaxWidth(40);
        TabelAkun.getColumnModel().getColumn(1).setMaxWidth(40);
    }

    private void clearInput() {
        inputNamaLengkap.setText("");
        inputUsername.setText("");
        inputPassword.setText("");
        inputJabatan.setSelectedIndex(0);

        selectedId = -1;
        btnSimpan.setText("Simpan");
    }

    private void simpanAkun() {
        String namaLengkap = inputNamaLengkap.getText();
        String username = inputUsername.getText();
        String password = String.valueOf(inputPassword.getPassword());
        String jabatan = String.valueOf(inputJabatan.getSelectedItem());

        if (namaLengkap.isEmpty() || username.isEmpty() || inputJabatan.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this,
                    "Nama Lengkap, Username, dan Jabatan wajib diisi.",
                    "Input Error",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (selectedId == -1 && password.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Password wajib diisi untuk membuat akun baru.",
                    "Input Error",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        String finalPasswordToSet = password;

        if (selectedId != -1 && password.isEmpty()) {
            User userLama = userDAO.getUserById(selectedId);
            if (userLama != null) {
                finalPasswordToSet = userLama.getPassword();
            }
        }

        User userBaru = new User();
        userBaru.setNamaLengkap(namaLengkap);
        userBaru.setUsername(username);
        userBaru.setPassword(finalPasswordToSet);
        userBaru.setJabatan(jabatan);

        boolean sukses;

        if (selectedId != -1) {
            userBaru.setId(selectedId);
            sukses = userDAO.update(userBaru);
        } else {
            sukses = userDAO.create(userBaru);
        }

        if (!sukses) {
            JOptionPane.showMessageDialog(this,
                    "Operasi database gagal. Kemungkinan username sudah ada atau masalah koneksi.",
                    "Gagal", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String pesan = (selectedId != -1) ? "Data akun berhasil diperbarui." : "Akun baru berhasil didaftarkan.";
        JOptionPane.showMessageDialog(this, pesan, "Sukses", JOptionPane.INFORMATION_MESSAGE);

        clearInput();
        loadTableData();
    }

    private void deleteAkun() {
        int selectedRow = TabelAkun.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus terlebih dahulu.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int idToDelete = (int) TabelAkun.getValueAt(selectedRow, 1);
        String namaAkun = (String) TabelAkun.getValueAt(selectedRow, 3);

        int konfirmasi = JOptionPane.showConfirmDialog(this,
                "Apakah Anda yakin ingin menghapus akun '" + namaAkun + "'?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            boolean sukses = userDAO.delete(idToDelete);

            if (!sukses) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data di database.", "Error", JOptionPane.ERROR_MESSAGE);
            }

            JOptionPane.showMessageDialog(this, "Data berhasil dihapus.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            loadTableData();
        }
    }

